package com.resume.model;

public class Resume {
    private String name;
    private String email;
    private String phone;
    private String highSchoolEducation;
    private String higherSecondaryEducation;
    private String undergraduateEducation;
    private String experience;
    private String skills;
    private String careerObjective;
    private String languagesKnown;

   
    public String getName() { return name; }
    public void setName(String name) { this.name = name; }

    public String getEmail() { return email; }
    public void setEmail(String email) { this.email = email; }

    public String getPhone() { return phone; }
    public void setPhone(String phone) { this.phone = phone; }

    public String getHighSchoolEducation() { return highSchoolEducation; }
    public void setHighSchoolEducation(String highSchoolEducation) { this.highSchoolEducation = highSchoolEducation; }

    public String getHigherSecondaryEducation() { return higherSecondaryEducation; }
    public void setHigherSecondaryEducation(String higherSecondaryEducation) { this.higherSecondaryEducation = higherSecondaryEducation; }

    public String getUndergraduateEducation() { return undergraduateEducation; }
    public void setUndergraduateEducation(String undergraduateEducation) { this.undergraduateEducation = undergraduateEducation; }

    public String getExperience() { return experience; }
    public void setExperience(String experience) { this.experience = experience; }

    public String getSkills() { return skills; }
    public void setSkills(String skills) { this.skills = skills; }

    public String getCareerObjective() { return careerObjective; }
    public void setCareerObjective(String careerObjective) { this.careerObjective = careerObjective; }

    public String getLanguagesKnown() { return languagesKnown; }
    public void setLanguagesKnown(String languagesKnown) { this.languagesKnown = languagesKnown; }
}
